public class Aritmetica {

    int a, b;

    //Constructor sin argumentos
    Aritmetica() {
    }

    //Constructor con argumentos
    Aritmetica(int a, int b) {
        this.a = a;
        this.b = b;
    }

    int sumar() {
        return a + b;
    }

    int restar() {
        return a - b;
    }

    int multiplicar() {
        return a * b;
    }

    int divivid() {
        return a / b;
    }

}

